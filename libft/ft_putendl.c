#include <unistd.h>
#include <string.h>

void	ft_putendl(char const *str)
{
	size_t i;

	i = 0;
	if (str == NULL)
		return ;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
