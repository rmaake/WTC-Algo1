#include <unistd.h>

void	ft_putnbr(long nbr)
{
	char c;

	c = '0';
	if (nbr < 0)
	{
		write(1, "-", 1);
		nbr = nbr * -1;
	}
	if (nbr >= 10)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
	{
		c = c + nbr;
		write(1, &c, 1);
	}
}
